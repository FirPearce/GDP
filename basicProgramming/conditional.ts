const hour = 12;
// If statement
if (hour < 12) {
  console.log("Good morning");
} else if (hour < 18) {
  console.log("Good day");
} else {
  console.log("Good evening");
}

const fruits = "Banana";

// switch case
switch (fruits) {
  case "Banana":
    console.log("Hello");
    break;
  case "Apple":
    console.log("Welcome");
    break;
}
